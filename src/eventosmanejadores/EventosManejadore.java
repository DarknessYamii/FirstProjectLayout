    private void Boton3SSBGMouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Boton3SSBGMouseMoved
          System.err.println("Cuidado con la epilepsia");  
        
        int colorSelector = (int) (Math.random()*5);
     
        switch(colorSelector){
            case 1: Background.setBackground(Color.red);
                    Boton3SSBG.setBackground(Color.blue);
                    Boton3SSBG.setSize(100,100);
                    break;
                    case 2: Background.setBackground(Color.blue);
                    Boton3SSBG.setBackground(Color.red);
                                        Boton3SSBG.setSize(70,70);
                    break;
                    case 3: Background.setBackground(Color.magenta);
                    Boton3SSBG.setBackground(Color.black);
                                        Boton3SSBG.setSize(80,80);
                    break;
                    case 4: Background.setBackground(Color.black);
                    Boton3SSBG.setBackground(Color.magenta);
                                        Boton3SSBG.setSize(90,90);
                    break;
                    case 5: Background.setBackground(Color.cyan);
                    Boton3SSBG.setBackground(Color.white);
                                        Boton3SSBG.setSize(110,110);
                    break;
                    default: Background.setBackground(Color.white);
                    Boton3SSBG.setBackground(Color.cyan);
                                        Boton3SSBG.setSize(60,60);
                    break;
                    
        }
    }//GEN-LAST:event_Boton3SSBGMouseMoved

